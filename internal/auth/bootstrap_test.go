func TestMaybeLogBootstrapCredential_AutoGenerated_RewriteIsIdempotent(t *testing.T) {
	// If the secret already exists (e.g. operator left it in place across
	// a re-bootstrap), the create-or-update path must succeed and refresh
	// the password without crashing.
	t.Setenv(EnvBootstrapAdminPassword, "")
	t.Setenv(EnvWriteInitialAdminSecret, "")

	const newPass = "RebootstrapXYZ123"
	s := newBootstrapStore(t, map[string][]byte{
		"admin.password":        []byte("$2a$10$fakebcrypt"),
		"admin.initialPassword": []byte(newPass),
	})

	// Pre-create the initial-admin-secret with stale data to simulate a
	// re-bootstrap scenario.
	if _, err := s.clientset.CoreV1().Secrets(testBootstrapNS).Create(context.Background(), &corev1Secret{
		ObjectMeta: metav1.ObjectMeta{Name: InitialAdminSecretName, Namespace: testBootstrapNS},
		Type:       corev1SecretTypeOpaque,
		Data:       map[string][]byte{"username": []byte("admin"), "password": []byte("STALE")},
	}, metav1.CreateOptions{}); err != nil {
		t.Fatalf("seed pre-existing initial-admin-secret: %v", err)
	}

	s.MaybeLogBootstrapCredential()

	got, err := s.clientset.CoreV1().Secrets(testBootstrapNS).Get(context.Background(), InitialAdminSecretName, metav1.GetOptions{})
	if err != nil {
		t.Fatalf("expected %s to exist after rewrite, got err=%v", InitialAdminSecretName, err)
	}
	if string(got.Data["password"]) != newPass {
		t.Errorf("password not refreshed: got %q want %q", got.Data["password"], newPass)
	}
	if got.Labels["app.kubernetes.io/managed-by"] != "sharko" {
		t.Errorf("labels not refreshed on update: %+v", got.Labels)
	}
}

