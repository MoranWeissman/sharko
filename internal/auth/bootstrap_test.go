func TestWriteInitialAdminSecretEnabled(t *testing.T) {
	// Default (unset) → true
	t.Setenv(EnvWriteInitialAdminSecret, "")
	if !writeInitialAdminSecretEnabled() {
		t.Errorf("default (unset) should enable initial-admin-secret writing")
	}
	// Explicit true → true
	t.Setenv(EnvWriteInitialAdminSecret, "true")
	if !writeInitialAdminSecretEnabled() {
		t.Errorf(`"true" should enable`)
	}
	// "false" → disabled
	t.Setenv(EnvWriteInitialAdminSecret, "false")
	if writeInitialAdminSecretEnabled() {
		t.Errorf(`"false" should disable`)
	}
	// "0" → disabled
	t.Setenv(EnvWriteInitialAdminSecret, "0")
	if writeInitialAdminSecretEnabled() {
		t.Errorf(`"0" should disable`)
	}
	// "no" → disabled
	t.Setenv(EnvWriteInitialAdminSecret, "no")
	if writeInitialAdminSecretEnabled() {
		t.Errorf(`"no" should disable`)
	}
	// Mixed-case false → disabled
	t.Setenv(EnvWriteInitialAdminSecret, "FALSE")
	if writeInitialAdminSecretEnabled() {
		t.Errorf(`"FALSE" should disable (case-insensitive)`)
	}
}
