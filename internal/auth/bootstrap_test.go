// captureSlog redirects the default slog logger to a buffer for the duration
// of the test, returning the buffer and a restore func.
func captureSlog(t *testing.T) (*bytes.Buffer, func()) {
	t.Helper()
	prev := slog.Default()
	buf := new(bytes.Buffer)
	slog.SetDefault(slog.New(slog.NewTextHandler(buf, &slog.HandlerOptions{Level: slog.LevelDebug})))
	return buf, func() { slog.SetDefault(prev) }
}

func newBootstrapStore(t *testing.T, secretData map[string][]byte) *Store {
	t.Helper()
	clientset := fake.NewSimpleClientset(&corev1.Secret{
		ObjectMeta: metav1.ObjectMeta{Name: testBootstrapSecret, Namespace: testBootstrapNS},
		Data:       secretData,
	})
	s := &Store{
		users:      make(map[string]*UserAccount),
		passHash:   make(map[string]string),
		userTokens: make(map[string]string),
		tokens:     make(map[string]*APIToken),
	}
	s.SetClientForTest(clientset, testBootstrapNS, testBootstrapSecret)
	return s
}

// --- MaybeLogBootstrapCredential ----------------------------------------------

func TestMaybeLogBootstrapCredential_AutoGenerated_LogsBlock(t *testing.T) {
	t.Setenv(EnvBootstrapAdminPassword, "")

	const generated = "GeneratedABC12345"
	s := newBootstrapStore(t, map[string][]byte{
		"admin.password":        []byte("$2a$10$fakebcrypt"),
		"admin.initialPassword": []byte(generated),
	})

	buf, restore := captureSlog(t)
	defer restore()

	s.MaybeLogBootstrapCredential()

	out := buf.String()
	if !strings.Contains(out, "BOOTSTRAP ADMIN CREDENTIAL") {
		t.Fatalf("expected BOOTSTRAP marker in logs, got: %s", out)
	}
	if !strings.Contains(out, generated) {
		t.Fatalf("expected generated password in logs, got: %s", out)
	}
	if !strings.Contains(out, "END BOOTSTRAP ADMIN CREDENTIAL") {
		t.Fatalf("expected END marker in logs, got: %s", out)
	}

	// admin.initialPassword should have been removed from the secret so that
	// the credential is not re-emitted on the next restart.
	updated, err := s.clientset.CoreV1().Secrets(testBootstrapNS).Get(context.Background(), testBootstrapSecret, metav1.GetOptions{})
	if err != nil {
		t.Fatalf("read secret after log: %v", err)
	}
	if _, present := updated.Data["admin.initialPassword"]; present {
		t.Fatalf("admin.initialPassword should be deleted after first log, but it persisted")
	}
}

func TestMaybeLogBootstrapCredential_OperatorSuppliedNotLogged(t *testing.T) {
	// CRITICAL security invariant: when the operator supplied a password via
	// env var (Helm `bootstrapAdmin.password` or `existingSecret`), the log
	// path MUST NOT emit anything — even if a stale admin.initialPassword
	// happens to be present in the secret.
	t.Setenv(EnvBootstrapAdminPassword, "OperatorSecret123")

	s := newBootstrapStore(t, map[string][]byte{
		"admin.password":        []byte("$2a$10$fakebcrypt"),
		"admin.initialPassword": []byte("StaleAutoGen99"),
	})

	buf, restore := captureSlog(t)
	defer restore()

	s.MaybeLogBootstrapCredential()

	out := buf.String()
	if strings.Contains(out, "BOOTSTRAP ADMIN CREDENTIAL") {
		t.Fatalf("operator-supplied path must NOT emit BOOTSTRAP block, got: %s", out)
	}
	if strings.Contains(out, "StaleAutoGen99") {
		t.Fatalf("operator-supplied path must NOT log any password, got: %s", out)
	}
	if strings.Contains(out, "OperatorSecret123") {
		t.Fatalf("operator-supplied password must NEVER appear in logs, got: %s", out)
	}
}

