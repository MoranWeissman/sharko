	// Required: `configuration:` lives at column 0 — the chart's own keys
	// are at the document root.
	if !strings.Contains(body, "\nconfiguration:") {
		t.Errorf("expected configuration: at document root, got:\n%s", body)
	}
	// Required: the per-cluster template block keeps the `# velero:`
