// TestVersionMismatch_All exercises the four cases the banner gates on.
func TestVersionMismatch_All(t *testing.T) {
	managed := ParsedHeader{Chart: "cert-manager", Version: "v1.15.1", Managed: true}
	unmanaged := ParsedHeader{Chart: "cert-manager", Version: "v1.15.1", Managed: false}

	cases := []struct {
		name       string
		catalog    string
		header     ParsedHeader
		wantCat    string
		wantValues string
	}{
		{name: "mismatch with managed=true", catalog: "v1.16.2", header: managed, wantCat: "v1.16.2", wantValues: "v1.15.1"},
		{name: "match suppresses banner", catalog: "v1.15.1", header: managed, wantCat: "", wantValues: ""},
		{name: "no managed=true header — banner suppressed", catalog: "v1.16.2", header: unmanaged, wantCat: "", wantValues: ""},
		{name: "v-prefix vs no-prefix counted as match", catalog: "1.15.1", header: managed, wantCat: "", wantValues: ""},
	}
	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			cat, val := VersionMismatch(tc.catalog, tc.header)
			if cat != tc.wantCat {
				t.Errorf("catalog: want %q got %q", tc.wantCat, cat)
			}
			if val != tc.wantValues {
				t.Errorf("values: want %q got %q", tc.wantValues, val)
			}
		})
	}
}

