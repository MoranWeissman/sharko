// TestClassifyClusterSpecificFields_PositiveAndNegative pins each
// pattern in clusterSpecificPatterns to a positive case (a path that
// must match) and a representative negative case (a path that must NOT
// match). The acceptance criterion for Story V121-6.1 requires both per
// pattern.
func TestClassifyClusterSpecificFields_PositiveAndNegative(t *testing.T) {
	cases := []struct {
		name        string
		yaml        string
		mustHit     []string // paths required to be classified cluster-specific
		mustNotHit  []string // paths that must NOT be classified cluster-specific
	}{
		{
			name: "ingress host + replicaCount + resources",
			yaml: `
ingress:
  enabled: true
  host: example.com
replicaCount: 2
resources:
  requests:
    cpu: 100m
    memory: 128Mi
image:
  repository: ghcr.io/example/app
`,
			mustHit:    []string{"ingress.host", "replicaCount", "resources.requests.cpu", "resources.requests.memory"},
			mustNotHit: []string{"image.repository"},
		},
		{
			name: "tls + persistence + storageClass + nodeSelector",
			yaml: `
tls:
  enabled: true
persistence:
  size: 10Gi
storageClass: gp3
nodeSelector:
  kubernetes.io/os: linux
`,
			mustHit: []string{"tls.enabled", "persistence.size", "storageClass", "nodeSelector"},
			mustNotHit: []string{},
		},
		{
			name: "domain endpoint url hostname",
			yaml: `
externalDatabase:
  host: db.example.com
  url: postgres://x
api:
  endpoint: https://example.com/api
domain: example.com
`,
			mustHit:    []string{"externalDatabase.host", "externalDatabase.url", "api.endpoint", "domain"},
			mustNotHit: []string{},
		},
		{
			name: "tolerations affinity externalSecret existingSecret region environment clusterName",
			yaml: `
tolerations:
  - key: dedicated
affinity:
  podAntiAffinity: {}
externalSecret:
  refreshInterval: 1h
existingSecret: my-secret
secretName: foo
clusterName: prod-eu
region: us-east-1
environment: production
`,
			mustHit: []string{
				"tolerations", "affinity",
				"externalSecret.refreshInterval", "existingSecret", "secretName",
				"clusterName", "region", "environment",
			},
			mustNotHit: []string{},
		},
		{
			name: "serviceAccount.annotations.* (IRSA)",
			yaml: `
serviceAccount:
  create: true
  annotations:
    eks.amazonaws.com/role-arn: arn:aws:iam::123456789012:role/foo
`,
			mustHit:    []string{"serviceAccount.annotations.eks.amazonaws.com/role-arn"},
			mustNotHit: []string{"serviceAccount.create"},
		},
	}

	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			got := ClassifyClusterSpecificFields([]byte(tc.yaml))
			gotSet := map[string]bool{}
			for _, g := range got {
				gotSet[g] = true
			}
			for _, want := range tc.mustHit {
				if !gotSet[want] {
					t.Errorf("expected %q to be classified cluster-specific, got %v", want, got)
				}
			}
			for _, no := range tc.mustNotHit {
				if gotSet[no] {
					t.Errorf("path %q must NOT be classified cluster-specific, got %v", no, got)
				}
			}
		})
	}
}

// TestSplitUpstreamValues_GlobalFileShape pins the v1.21 Bundle 5 shape:
// upstream defaults preserved at TOP LEVEL with cluster-specific scalars
// replaced by a commented placeholder, plus a trailing per-cluster template
// block (still wrapped under the addon name because that block is meant to
// be copy-pasted into the per-cluster file).
func TestSplitUpstreamValues_GlobalFileShape(t *testing.T) {
	upstream := []byte(`# Upstream comment
ingress:
  enabled: true
  host: example.com
replicaCount: 2
image:
  repository: ghcr.io/example/app
`)

	out := SplitUpstreamValues(SmartValuesInput{
		AddonName:      "cert-manager",
		Chart:          "cert-manager",
		Version:        "v1.14.4",
		RepoURL:        "https://charts.jetstack.io",
		UpstreamValues: upstream,
	})

	body := string(out.File)

	// Header lines + format-shape comment.
	for _, want := range []string{
		"# Generated by Sharko from cert-manager@v1.14.4 on ",
		"# Chart source: https://charts.jetstack.io",
		"# AI annotation: disabled",
		"# sharko: managed=true",
		"# Helm values for cert-manager",
	} {
		if !strings.Contains(body, want) {
			t.Errorf("expected output to contain %q\n--- output ---\n%s", want, body)
		}
	}

	// CRITICAL: there must NOT be an `<addonName>:` wrap around the body.
	// Lines starting at column 0 with `cert-manager:` are forbidden — the
	// file is meant to be passed directly to Helm via `valueFiles:`.
	for _, line := range strings.Split(body, "\n") {
		if line == "cert-manager:" {
			t.Fatalf("global file must NOT be wrapped under <addon>: at the body root, got:\n%s", body)
		}
	}

	// `ingress.host` becomes a comment placeholder. Indentation now
	// matches the upstream (no extra two-space wrap), so `# host:` is
	// indented exactly two spaces as in the upstream.
	if !strings.Contains(body, "  # host: <cluster-specific>") {
		t.Errorf("expected commented host placeholder at upstream indent, got:\n%s", body)
	}
	// `replicaCount` becomes a comment placeholder at column 0 (top-level
	// scalar in the chart values).
	if !strings.Contains(body, "\n# replicaCount: <cluster-specific>") {
		t.Errorf("expected top-level commented replicaCount placeholder, got:\n%s", body)
	}
	// `image.repository` survives at upstream indent.
	if !strings.Contains(body, "  repository: ghcr.io/example/app") {
		t.Errorf("expected image.repository to survive at upstream indent, got:\n%s", body)
	}
	// Upstream comment passed through verbatim.
	if !strings.Contains(body, "# Upstream comment") {
		t.Errorf("expected upstream comment to be preserved, got:\n%s", body)
	}
	// Template block at the bottom — still wrapped under the addon name
	// because that block is the per-cluster overrides template.
	if !strings.Contains(body, "# --- per-cluster overrides template ---") {
		t.Errorf("missing template block marker, got:\n%s", body)
	}
	if !strings.Contains(body, "# cert-manager:") {
		t.Errorf("missing addon header inside template block, got:\n%s", body)
	}
	// Template includes the cluster-specific leaves we found.
	if !strings.Contains(body, `"ingress.host": <set per cluster>`) {
		t.Errorf("missing ingress.host template line, got:\n%s", body)
	}
	if !strings.Contains(body, "replicaCount: <set per cluster>") {
		t.Errorf("missing replicaCount template line, got:\n%s", body)
	}

	// ClusterSpecificPaths matches what we found.
	if len(out.ClusterSpecificPaths) == 0 {
		t.Errorf("expected at least one cluster-specific path, got empty")
	}
}

