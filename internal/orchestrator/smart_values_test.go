// TestWriteAndParseSmartValuesHeader_Roundtrip ensures the header can
// round-trip through the writer + parser, including the optional
// per-addon AI opt-out directive (Story V121-6.3 + Story V121-7.3).
func TestWriteAndParseSmartValuesHeader_Roundtrip(t *testing.T) {
	now := time.Date(2026, 4, 17, 12, 0, 0, 0, time.UTC)

	for _, tc := range []struct {
		name string
		h    SmartValuesHeader
	}{
		{
			name: "ai annotated, no opt-out",
			h: SmartValuesHeader{
				Chart: "cert-manager", Version: "v1.14.4",
				RepoURL:     "https://charts.jetstack.io",
				AIAnnotated: true, Now: now,
			},
		},
		{
			name: "ai disabled with per-addon opt-out",
			h: SmartValuesHeader{
				Chart: "external-secrets", Version: "v0.10.0",
				RepoURL:     "https://charts.external-secrets.io",
				AIAnnotated: false, AIOptOut: true, Now: now,
			},
		},
	} {
		t.Run(tc.name, func(t *testing.T) {
			out := WriteSmartValuesHeader(tc.h)
			parsed := ParseSmartValuesHeader([]byte(out))
			if parsed.Chart != tc.h.Chart {
				t.Errorf("chart: want %q got %q", tc.h.Chart, parsed.Chart)
			}
			if parsed.Version != tc.h.Version {
				t.Errorf("version: want %q got %q", tc.h.Version, parsed.Version)
			}
			if parsed.RepoURL != tc.h.RepoURL {
				t.Errorf("repo: want %q got %q", tc.h.RepoURL, parsed.RepoURL)
			}
			if parsed.AIAnnotated != tc.h.AIAnnotated {
				t.Errorf("AIAnnotated: want %v got %v", tc.h.AIAnnotated, parsed.AIAnnotated)
			}
			if parsed.AIOptOut != tc.h.AIOptOut {
				t.Errorf("AIOptOut: want %v got %v", tc.h.AIOptOut, parsed.AIOptOut)
			}
			if !parsed.Managed {
				t.Errorf("expected Managed=true, got false")
			}
		})
	}
}

